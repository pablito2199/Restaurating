    public void regresoVentana() {
        switch (ventana) {
            case "carrito": {
                carrito vent = new carrito(ventana1, sesion);
                vent.setLocationRelativeTo(null);
                vent.setVisible(true);
                this.dispose();
                break;
            }
            case "principal": {
                principal vent = new principal(sesion);
                vent.comprobarProductos();
                vent.setLocationRelativeTo(null);
                vent.setVisible(true);
                this.dispose();
                break;
            }
            default:
                break;
        }
    }

    private void cambioPrecio() {
        if ((primero1.isFocusable() || primero2.isFocusable()) && (segundo1.isFocusable() || segundo2.isFocusable())) {
            precio = 9.50 * Integer.parseInt(cantidadMenu.getText());
        } else if (primero1.isFocusable() || primero2.isFocusable() || segundo1.isFocusable() || segundo2.isFocusable()) {
            precio = 5.50 * Integer.parseInt(cantidadMenu.getText());
        } else {
            precio = 0.0;
        }
        precio = Math.round(precio * 100.0) / 100.0;
        precioCambiado.setText(getTwoDecimals(precio) + " €");
    }

    private void botonPedir() {
        if (precio != 0.0) {
            btnAnadir.setEnabled(true);
        } else {
            btnAnadir.setEnabled(false);
        }

        if (!btnAnadir.isEnabled()) {
            avisoPago.setText("*Debe añadir un primero o un segundo como mínimo.");
        } else {
            avisoPago.setText("");
        }
    }

    private static String getTwoDecimals(double value) {
        DecimalFormat df = new DecimalFormat("0.00");
        return df.format(value);
    }
