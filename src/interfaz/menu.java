    private void btnAnadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAnadirActionPerformed
        Menu menu = new Menu();
        if (primero1.isFocusable()) {
            menu.setEnsaladilla("Ensaladilla Rusa");
        } else if (primero2.isFocusable()) {
            menu.setCroquetas("Croquetas de Atún");
        }
        if (segundo1.isFocusable()) {
            menu.setCachopo("Cachopo de Ternera");
        } else if (segundo2.isFocusable()) {
            menu.setSalmon("Salmón al Horno");
        }
        if (postre.isFocusable()) {
            menu.setPostre("Postre Sorpresa");
        } else if (cafe.isFocusable()) {
            menu.setCafe("Café");
        }
        menu.setPrecio(this.precio);

        String menuS = "";
        if (menu.getEnsaladilla() != null) {
            menuS += "Ensaladilla Rusa";
            if (menu.getPostre() != null || menu.getCachopo() != null || menu.getSalmon() != null || menu.getCafe() != null) {
                menuS += " + ";
            }
        }
        if (menu.getCroquetas() != null) {
            menuS += "Croquetas de Atún";
            if (menu.getPostre() != null || menu.getCachopo() != null || menu.getSalmon() != null || menu.getCafe() != null) {
                menuS += " + ";
            }
        }
        if (menu.getCachopo() != null) {
            menuS += "Cachopo de Ternera";
            if (menu.getPostre() != null || menu.getCafe() != null) {
                menuS += " + ";
            }
        }
        if (menu.getSalmon() != null) {
            menuS += "Salmón al Horno";
            if (menu.getPostre() != null || menu.getCafe() != null) {
                menuS += " + ";
            }
        }
        if (menu.getPostre() != null) {
            menuS += "Postre Sorpresa";
        }
        if (menu.getCafe() != null) {
            menuS += "Café";
        }

        menu.setProductos(menuS);
        menu.setCantidad(Integer.parseInt(cantidadMenu.getText()));
        sesion.menu.add(menu);
        regresoVentana();
        JOptionPane.showMessageDialog(null, "Menú añadido correctamente.");
    }//GEN-LAST:event_btnAnadirActionPerformed

