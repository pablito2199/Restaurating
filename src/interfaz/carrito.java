    private void btnEliminarSeleccionadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarSeleccionadoActionPerformed
        precio = 0.0;

        if (tablaCarrito.getValueAt(tablaCarrito.getSelectedRow(), 0).equals("Carta")) {
            for (int i = 0; i < sesion.productosCarta.size(); i++) {
                if (sesion.productosCarta.get(i).getProducto().equals(tablaCarrito.getValueAt(tablaCarrito.getSelectedRow(), 1))) {
                    sesion.productosCarta.get(i).setCantidad(0);
                    break;
                }
            }
        } else {
            for (int i = 0; i < sesion.menu.size(); i++) {
                if (sesion.menu.get(i).getProductos().equals(tablaCarrito.getValueAt(tablaCarrito.getSelectedRow(), 1))) {
                    sesion.menu.remove(i);
                    break;
                }
            }
        }

        DefaultTableModel modelo = (DefaultTableModel) tablaCarrito.getModel();
        modelo.removeRow(tablaCarrito.getSelectedRow());
        tablaCarrito.setModel(modelo);

        for (Menu m : sesion.menu) {
            precio += m.getPrecio();
        }
        for (ProductoCarta p : sesion.productosCarta) {
            if (p.getCantidad() != 0) {
                precio += p.getCantidad() * p.getPrecio();
            }
        }
        precio = Math.round(precio * 100.0) / 100.0;
        precioCambiado.setText(getTwoDecimals(precio) + " €");

        modelo.fireTableDataChanged();

        if (modelo.getRowCount() > 0) {
            tablaCarrito.setRowSelectionInterval(0, 0);
            btnEliminarSeleccionado.setEnabled(true);
            btnEliminarTodo.setEnabled(true);
            btnCaja.setEnabled(true);
        } else {
            btnEliminarSeleccionado.setEnabled(false);
            btnEliminarTodo.setEnabled(false);
            btnCaja.setEnabled(false);
        }
    }//GEN-LAST:event_btnEliminarSeleccionadoActionPerformed

