    public void regresoVentana(String ventana) {
        switch (ventana) {
            case "configuracion": {
                configuracion vent = new configuracion(sesion, "carrito", ventana);
                vent.setLocationRelativeTo(null);
                vent.setVisible(true);
                break;
            }
            case "menu": {
                menu vent = new menu(sesion, "carrito", ventana);
                vent.setLocationRelativeTo(null);
                vent.setVisible(true);
                break;
            }
            case "principal": {
                principal vent = new principal(sesion);
                vent.comprobarProductos();
                vent.setLocationRelativeTo(null);
                vent.setVisible(true);
                break;
            }
            default:
                break;
        }
    }

    public void comprobarProductos() {
        boolean carta = false;
        for (ProductoCarta prod : sesion.productosCarta) {
            if (prod.getCantidad() > 0) {
                carta = true;
                break;
            }
        }

        if (carta || sesion.menu.size() > 0) {
            btnCaja.setEnabled(true);
            btnEliminarTodo.setEnabled(true);
        } else {
            btnCaja.setEnabled(false);
            btnEliminarTodo.setEnabled(false);
        }

        if (!btnCaja.isEnabled()) {
            avisoPago.setText("*Debe añadir algún producto al carrito para poder gestionar el carrito.");
        } else {
            avisoPago.setText("");
        }
    }

    private void actualizarTabla() {
        precio = 0.0;

        DefaultTableModel modelo = (DefaultTableModel) tablaCarrito.getModel();
        for (Menu m : sesion.menu) {
            String menu = "";
            if (m.getEnsaladilla() != null) {
                menu += "Ensaladilla Rusa";
                if (m.getPostre() != null || m.getCachopo() != null || m.getSalmon() != null || m.getCafe() != null) {
                    menu += " + ";
                }
            }
            if (m.getCroquetas() != null) {
                menu += "Croquetas de Atún";
                if (m.getPostre() != null || m.getCachopo() != null || m.getSalmon() != null || m.getCafe() != null) {
                    menu += " + ";
                }
            }
            if (m.getCachopo() != null) {
                menu += "Cachopo de Ternera";
                if (m.getPostre() != null || m.getCafe() != null) {
                    menu += " + ";
                }
            }
            if (m.getSalmon() != null) {
                menu += "Salmón al Horno";
                if (m.getPostre() != null || m.getCafe() != null) {
                    menu += " + ";
                }
            }
            if (m.getPostre() != null) {
                menu += "Postre Sorpresa";
            }
            if (m.getCafe() != null) {
                menu += "Café";
            }
            Object[] fila = {"Menú", menu, m.getCantidad(), getTwoDecimals(m.getPrecio()) + " €"};
            modelo.addRow(fila);

            precio += m.getPrecio();
        }

        for (ProductoCarta p : sesion.productosCarta) {
            if (p.getCantidad() != 0) {
                Double pr = p.getCantidad() * p.getPrecio();
                pr = Math.round(pr * 100.0) / 100.0;
                Object[] fila = {"Carta", p.getProducto(), p.getCantidad(), getTwoDecimals(pr) + " €"};
                modelo.addRow(fila);

                precio += p.getCantidad() * p.getPrecio();
            }
        }

        precio = Math.round(precio * 100.0) / 100.0;
        precioCambiado.setText(getTwoDecimals(precio) + " €");

        modelo.fireTableDataChanged();

        if (modelo.getRowCount() > 0) {
            tablaCarrito.setRowSelectionInterval(0, 0);
            btnEliminarSeleccionado.setEnabled(true);
            btnEliminarTodo.setEnabled(true);
            btnCaja.setEnabled(true);
        } else {
            btnEliminarSeleccionado.setEnabled(false);
            btnEliminarTodo.setEnabled(false);
            btnCaja.setEnabled(false);
        }
    }

    private static String getTwoDecimals(double value) {
        DecimalFormat df = new DecimalFormat("0.00");
        return df.format(value);
    }
